
class Calc:

   def add(self, x, y):
       self.x = x
       self.y = y
       a = self.x + self.y
       return a

   def subtract(self, x, y):
       self.x = x
       self.y = y
       a = self.x - self.y
       return a    

   def multiply(self, x, y):
       self.x = x
       self.y = y
       a = self.x * self.y
       return a    

   def divide(self, x, y):      
       self.x = x
       self.y = y
       a = self.x / self.y
       return a
